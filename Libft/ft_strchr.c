#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;
	int		i;
	int		leng_s;

	str = (char *)s;
	i = 0;
	leng_s = ft_strlen(s);
	while (i < leng_s)
	{
		if (str[i] == c)
			return (&str[i]);
		i++;
	}
	if (c == '\0')
		return (&str[i]);
	return (NULL);
}
